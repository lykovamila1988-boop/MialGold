def gumroad_sales():
    if not GUMROAD_TOKEN: return "⚠️ Нет GUMROAD_ACCESS_TOKEN в .env"
    try:
        r = requests.get("https://api.gumroad.com/v2/sales",
                        params={"access_token": GUMROAD_TOKEN}, timeout=10)
        return json.dumps(r.json().get("sales", [])[:10], indent=2, ensure_ascii=False)
    except Exception as e: return f"Gumroad недоступен: {e}"

def handle(name, inp):
    if name == "gumroad_sales": return gumroad_sales()
    res = core_handle(name, inp)
    return res if res is not None else f"Неизвестный инструмент: {name}"

